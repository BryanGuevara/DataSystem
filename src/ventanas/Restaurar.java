    private void ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ActualizarActionPerformed

        String password, confirmacion;

        password = TxtPassword1.getText().trim();
        confirmacion = TxtPassword.getText().trim();

        if (!password.equals("") && !confirmacion.equals("")) {

            if (password.equals(confirmacion)) {

                try {

                    Connection cn = Conexiones.conexion();
                    PreparedStatement pst = cn.prepareStatement(
                            "update usuarios set password=? where Username = '" + update + "'");

                    pst.setString(1, password);
                    cn.close();
                    TxtPassword.setBackground(Color.green);
                    TxtPassword1.setBackground(Color.green);
                    JOptionPane.showMessageDialog(null, "La actualizacion a salido exitosa");
                    this.dispose();

                } catch (Exception e) {
                    System.out.println(e);
                }

            } else {
                TxtPassword.setBackground(Color.blue);
                TxtPassword1.setBackground(Color.red);
                JOptionPane.showMessageDialog(null, "Las contrase単as no coinciden");
            }

        } else {
            TxtPassword.setBackground(Color.red);
            TxtPassword1.setBackground(Color.red);
            JOptionPane.showMessageDialog(null, "Porfavor llene todos los campos");
        }

    }//GEN-LAST:event_ActualizarActionPerformed

